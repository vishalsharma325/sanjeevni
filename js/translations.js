// Get current language from localStorage or default to English
let currentLanguage = localStorage.getItem('selectedLanguage') || 'en';

// Translation function
function t(key) {
    return translations[currentLanguage][key] || translations['en'][key] || key;
}

// Change language function
function changeLanguage(lang) {
    if (translations[lang]) {
        currentLanguage = lang;
        localStorage.setItem('selectedLanguage', lang);
        updatePageContent();
    }
}

// Update all text content on the page
function updatePageContent() {
    // Update elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = t(key);
        
        if (element.tagName === 'INPUT' && (element.type === 'text' || element.type === 'email' || element.type === 'password')) {
            element.placeholder = translation;
        } else if (element.tagName === 'TEXTAREA') {
            element.placeholder = translation;
        } else {
            element.textContent = translation;
        }
    });
    
    // Update elements with data-translate-placeholder attribute  
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        const translation = t(key);
        element.placeholder = translation;
    });
    
    // Update current language display
    const currentLangElement = document.getElementById('current-lang');
    if (currentLangElement) {
        const langNames = {
            'en': 'English',
            'hi': 'हिंदी', 
            'pa': 'ਪੰਜਾਬੀ'
        };
        currentLangElement.textContent = langNames[currentLanguage] || 'English';
    }
    
    // Update document title
    const titleElement = document.querySelector('[data-translate="login.title"]');
    if (titleElement) {
        document.title = t('login.title');
    } else if (document.querySelector('[data-translate="nav.home"]')) {
        document.title = `${t('nav.home')} - Punjab e-Health`;
    }
    
    // Update HTML lang attribute
    document.documentElement.lang = currentLanguage === 'pa' ? 'pa-IN' : currentLanguage === 'hi' ? 'hi-IN' : 'en-IN';
}

// Initialize translations
function initializeTranslations() {
    updatePageContent();
}

// Initialize translations when DOM loads
document.addEventListener('DOMContentLoaded', function() {
    updatePageContent();
});

// Export functions for global use
window.translations = {
    t,
    changeLanguage,
    updatePageContent,
    initializeTranslations,
    currentLanguage: () => currentLanguage
};

// Make functions available globally
window.t = t;
window.changeLanguage = changeLanguage;
window.updatePageContent = updatePageContent;
window.initializeTranslations = initializeTranslations;

